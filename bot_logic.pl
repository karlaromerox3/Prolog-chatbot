%% Receives a list and chooses a random item of it
%% Returns the item of the list
random_n(Res, R):- 
        length(Res, Length),  
        Upper is Length + 1,
        random(1, Upper, Rand),
        get_item(Res, Rand, R).

%% prints a sentence from a list format printing spaces after each Header
print_sentence([]):- nl.
print_sentence([H|T]):- write(H), write(' '), print_sentence(T).

